a = "Aritra Roy"
b = ' is a good boy.'
print(a+b)  # Concatenation of Strings
c = [1, 3, 5]
d = [7, 9, 11]
print(d+c)  # Putting together two lists into one list
print(11/85)
print(10+13*4/2)  # Displaying Operator Precendence
# Arithmetic Operators : +, -, *, /, //, %, **
print('Floor Division Operator returns integer part on division : ', 7 // 2)
print('Modulus operator :', 7 % 3)
print('Exponential operator : ', 6**2)
print('-------------------------------------')
# Relational Operators : >, <, >=, <=, ==, != [They always return boolean values]
print(5 < 10)
print(48 >= 154)
print(64 <= 66)
print(88 == 88)  # Equal to Operator
print(48 != 48)  # Not Equal To Operator
print('-------------------------------------')
# Logical Operators : and, or, not
print(True and True)  # and operator
print(True and False)
print(False and False)
print(False and True)
print(True or True)  # or operator
print(True or False)
print(False or False)
print(False or True)
# not operator - It can be used either with or without brackets, as we see here
print(not (True))
print(not False)
